function perkalian(p1, p2) {
    return p1 * p2;
  }
  
  let result = perkalian(3, 3);  // 3 * 3 = 9
  let result2 = perkalian(8, 3);  // 8 * 3 = 24
  console.log(result);  // Output: 9
  console.log(result2);  // Output: 24
  
  function coba(angka1, angka2, angka3) {
    return (angka1 * angka2) / angka3;
  }
  
  let result3 = coba(2, 8, 4);  // (2 * 8) / 4 = 4
  console.log(result3);  // Output: 4
  